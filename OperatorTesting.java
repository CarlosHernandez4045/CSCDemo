public class OperatorTesting {
    
    public static void main(String[] args) {
        int b = 10;

        while(b < 10){
            System.out.println("The indicated variable is less than 10");
            return;
        }
        
        while(b > 10){
            System.out.println("The indicated variable is more than 10");
            return;
        }

        while(b == 10){
            System.out.println("The indicated variable is equal to 10");
            return;
        }
    }
}
